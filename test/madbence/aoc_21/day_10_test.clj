(ns madbence.aoc-21.day-10-test
  (:require [clojure.test :refer [deftest is]]
            [madbence.aoc-21.day-10 :refer [a b get-score get-score']]))

(def fixture "[({(<(())[]>[[{[]{<()<>>
[(()[<>])]({[<{<<[]>>(
{([(<{}[<>[]}>{[]{[(<()>
(((({<>}<{<{<>}{[]{[]{}
[[<[([]))<([[{}[[()]]]
[{[{({}]{}}([{[{{{}}([]
{<[[]]>}<{[{[{[]{()[[[]
[<(<(<(<{}))><([]([]()
<{([([[(<>()){}]>(<<{{
<{([{{}}[<[[[<>{}]]]>[]]")

(deftest test-get-score
  (is (= (get-score "{([(<{}[<>[]}>{[]{[(<()>") 1197))
  (is (= (get-score "[[<[([]))<([[{}[[()]]]") 3))
  (is (= (get-score "[{[{({}]{}}([{[{{{}}([]") 57))
  (is (= (get-score "[<(<(<(<{}))><([]([]()") 3))
  (is (= (get-score "<{([([[(<>()){}]>(<<{{") 25137)))

(deftest test-get-score'
  (is (= (get-score' "[({(<(())[]>[[{[]{<()<>>") 288957))
  (is (= (get-score' "[(()[<>])]({[<{<<[]>>(") 5566))
  (is (= (get-score' "(((({<>}<{<{<>}{[]{[]{}") 1480781))
  (is (= (get-score' "{<[[]]>}<{[{[{[]{()[[[]") 995444))
  (is (= (get-score' "<{([{{}}[<[[[<>{}]]]>[]]") 294)))

(deftest test-a
  (is (= (a fixture) 26397)))

(deftest test-b
  (is (= (b fixture) 288957)))
